
public class Employee {
    private int empId;
    private String empFirstName;
    private String empMdName;
    private String empLsName;
    private int empAge;
    private String empDept;
    private String empAddr;

    public int getEmpId() {
        return empId;
    }

    public void setEmpId(int empId) {
        this.empId = empId;
    }

    public String getEmpFirstName() {
        return empFirstName;
    }

    public void setEmpFirstName(String empFirstName) {
        this.empFirstName = empFirstName;
    }

    public String getEmpMdName() {
        return empMdName;
    }

    public void setEmpMdName(String empMdName) {
        this.empMdName = empMdName;
    }

    public String getEmpLsName() {
        return empLsName;
    }

    public void setEmpLsName(String empLsName) {
        this.empLsName = empLsName;
    }

    public int getEmpAge() {
        return empAge;
    }

    public void setEmpAge(int empAge) {
        this.empAge = empAge;
    }

    public String getEmpDept() {
        return empDept;
    }

    public void setEmpDept(String empDept) {
        this.empDept = empDept;
    }

    public String getEmpAddr() {
        return empAddr;
    }

    public void setEmpAddr(String empAddr) {
        this.empAddr = empAddr;
    }

}
